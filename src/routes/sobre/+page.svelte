  <h2>Uso de IA no Desenvolvimento do Projeto</h2>
  <p>Durante o desenvolvimento do jogo "Labirinto", a equipe utilizou diversas Inteligências Artificiais como ferramentas de apoio para otimizar o trabalho e melhorar a qualidade do projeto.</p>
  <p>A IA principal que auxiliou na maior parte das etapas foi o ChatGPT, que foi essencial para revisar, sugerir melhorias no código e explicar conceitos de programação, especialmente em relação ao uso de TypeScript, HTML, CSS e Svelte.</p>
  <p>Além disso, a equipe recorreu à IA Gemini para pesquisas e soluções rápidas relacionadas ao desenvolvimento do jogo, e à IA do Canva para a criação de imagens e elementos gráficos que compõem o visual do projeto.</p>
  <p>Com o auxílio dessas tecnologias, a equipe conseguiu focar mais nas decisões criativas e na implementação de funcionalidades, enquanto as IAs ofereceram suporte técnico e criativo, garantindo que o projeto fosse entregue com mais qualidade e eficiência.</p>
